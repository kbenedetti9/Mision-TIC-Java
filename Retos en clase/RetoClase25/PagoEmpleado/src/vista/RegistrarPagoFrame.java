    public String getJComboBox(int numero) {
        String valor = "";
        if (numero == 1) {
            valor = cbAno.getSelectedItem().toString();
        } else if (numero == 2) {
            valor = cbMes.getSelectedItem().toString();
        }
        return valor;
    }

