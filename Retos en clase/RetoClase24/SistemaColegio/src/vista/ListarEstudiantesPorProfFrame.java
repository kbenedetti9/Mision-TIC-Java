    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        String profesorSeleccionado = cmbProfesores.getSelectedItem().toString();
        String id = profesorSeleccionado.split(":")[0];
        long idProfesor = Long.parseLong(id);
        listaEstudiantesDeProf = Estudiante.consultarEstudiantePorProfesor(idProfesor);
        llenarListaEstudiantes();
    }//GEN-LAST:event_btnConsultarActionPerformed

    public void llenarComboProfesor() {
        Profesor profesor = new Profesor();
        DefaultComboBoxModel modeloLista = new DefaultComboBoxModel();
        modeloLista.removeAllElements();
        listaProfesores = profesor.leer();
        modeloLista.addElement("Seleccionar");
        for (Profesor profesorRecorrido : listaProfesores) {
            modeloLista.addElement(profesorRecorrido.getId() + ": " + profesorRecorrido.getNombre());
        }
        cmbProfesores.setModel(modeloLista);
    }

    public void llenarListaEstudiantes() {
        DefaultListModel modeloLista = new DefaultListModel();
        for (Estudiante estudianteRecorrido : listaEstudiantesDeProf) {
            modeloLista.addElement(estudianteRecorrido.getId() + ": " + estudianteRecorrido.getNombre());
        }
        lstEstudiantesDeProf.setModel(modeloLista);
    }

